private void tampilkan_data(){
DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("ID Bandar");
        model.addColumn("Nama Bandar");
        model.addColumn("No Ponsel");
        
        try {
            int no=1;
            String cari = txtCari.getText();
            String sql = "select * from bandar where nama_bandar like '%"+cari+"%' order by id_bandar desc";
            Connection conn=(Connection)koneksi.getKoneksi();
            Statement stm=conn.createStatement();
            ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3)});
            }
            tabelBandar.setModel(model);
        } catch (Exception e) {
        }

        btnSimpan.setEnabled(true);
        btnEdit.setEnabled(false);
        btnHapus.setEnabled(false);
        btnKembali.setEnabled(true);
}
       
   private void AutoNumber(){
         try{
          String sql = "SELECT MAX(RIGHT(id_bandar,3)) AS no_auto FROM bandar";
          Connection conn = (Connection)koneksi.getKoneksi();
          Statement stm= conn.createStatement(); 
          ResultSet res = stm.executeQuery(sql);
            while(res.next()){         
           if (res.first()==false){
               txtId.setText("BDR001");
           }else{
               res.last();
               int set_kode = res.getInt(1)+1;
               String no_auto = String.valueOf(set_kode);
               int kode_next = no_auto.length();
               for (int a=0; a<3 - kode_next; a++){
                   no_auto ="0"+no_auto;
               }
               txtId.setText("BDR"+no_auto);
           }
           } 
         }catch (SQLException ex){
            Logger.getLogger(FormBandar.class.getName()).log(Level.SEVERE, null, ex);
          
    
     }
   }
   
