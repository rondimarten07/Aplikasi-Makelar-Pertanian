    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        
        String IdBandar = txtId.getText();
        String NamaBandar = txtNama.getText();
        String NoPonsel = txtPonsel.getText();
        

        if (IdBandar.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Id Bandar tidak boleh kosong");
            txtId.requestFocus();
        }else if (NamaBandar.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Nama Bandar tidak boleh kosong");
            txtNama.requestFocus();
        }else if (NoPonsel.isEmpty()) {
            JOptionPane.showMessageDialog(null,"No Ponsel tidak boleh kosong");
            txtPonsel.requestFocus();
        }else{
            try{
                Connection conn=(Connection)koneksi.getKoneksi();
                Statement stm=conn.createStatement();
               
                stm.executeUpdate("insert into bandar VALUES('"+IdBandar+"','"+NamaBandar+"','"+NoPonsel+"')");
                
                tampilkan_data();
                kosongkan_form();
                AutoNumber();
       
                 JOptionPane.showMessageDialog(this,"Data berhasil disimpan","Success",JOptionPane.INFORMATION_MESSAGE);
            } catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Simpan data gagal\n"+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

